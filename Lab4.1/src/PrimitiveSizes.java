public class PrimitiveSizes {
	public static void main(String[] args){
		System.out.println("An boolean variable is 1 bit, and does not have a size constant");
		System.out.println("An char variable is " + Character.SIZE + " bits.");
		System.out.println("An byte variable is " + Byte.SIZE + " bits.");
		System.out.println("An short variable is " + Short.SIZE + " bits.");
		System.out.println("An int variable is " + Integer.SIZE + " bits.");
		System.out.println("An long variable is " + Long.SIZE + " bits.");
		System.out.println("An float variable is " + Float.SIZE + " bits.");
		System.out.println("An double variable is " + Double.SIZE + " bits.");
	}

}
